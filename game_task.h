void saveGameName()
{

    printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");

    FILE *fPtr;
    char dataToAdd[BUFFER_SIZE];

    /*  Open all file in append mode. */
    fPtr = fopen("save.txt", "a");


    /* fopen() return NULL if unable to open file in given mode. */
    if (fPtr == NULL)
    {
        /* Unable to open file hence exit */
        printf("\nUnable to open '%s' file.\n", "save.txt");
        exit(EXIT_FAILURE);
    }


    /* Input data to append from user */
    printf("\n\t\t\t\t\t\t\tEnter your name: ");
    fflush(stdin);          // To clear extra white space characters in stdin
    fgets(dataToAdd, BUFFER_SIZE, stdin);


    /* Append data to file */
    fputs(dataToAdd, fPtr);

    printf("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");

    /* Reopen file in read mode to print file contents */
   // fPtr = freopen("save.txt", "r", fPtr);

    //Print file contents after appending string
    printf("\t\t\t\t\t\t\tYour name added below :\n\n");
    /* Done with file, hence close file. */
    //readFile(fPtr);
    return_top_player();
    fclose(fPtr);
    printf("\n\033[0m+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");

    /*  Open all file in append mode. */
    Sleep(25000);
    return 0;
}

